// Ember ramp (canvas needs concrete colors; values mirror the CSS tokens).
const AMBER: [number, number, number] = [247, 160, 62];
const EMBER: [number, number, number] = [242, 121, 95];
const PINK: [number, number, number] = [238, 93, 155];
export const INK_FACE = "#1E1524";
export const INK_EDGE = "rgba(238,93,155,0.8)";
export const CANVAS_BG = "#0E0B12";

const mix = (a: number[], b: number[], f: number) =>
  a.map((v, i) => Math.round(v + (b[i] - v) * f));

/** Pre-bucketed ember palette (avoids per-frame color-string churn). */
export const EMBER_PALETTE: string[] = Array.from({ length: 32 }, (_, i) => {
  const t = i / 31;
  const c = t < 0.5 ? mix(AMBER, EMBER, t * 2) : mix(EMBER, PINK, (t - 0.5) * 2);
  return `rgb(${c[0]},${c[1]},${c[2]})`;
});

export const emberColor = (t: number): string =>
  EMBER_PALETTE[Math.max(0, Math.min(31, Math.round(t * 31)))];
